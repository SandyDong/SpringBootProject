package com.dong.entity;

/**
 * 创建一个用户实体类
 */
public class User {

    private String id;

    private String name;

    private String sex;

    private int age;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getAge() {
        return age;
    }
}
